// ── реплики ──
// speaker: 'raven' | 'player' — чей портрет/имя показывать
// text:    что говорит ({player}/{raven} заменятся на имена)
export const DIALOGUES = {

  // ── ВСТУПЛЕНИЕ ── играется в начале партии, мир на паузе.
  // Последняя реплика («...Так. А это ещё кто?») заканчивает диалог —
  // мир оживает, и тут же подхватывает обучение (см. tutorial.js).
  intro: [
    { id: 'intro_1', speaker: 'raven', text: 'Великий Один вновь устроил пир, {player}.' },
    { id: 'intro_2', speaker: 'raven', text: 'А нам с тобой досталась западная стена Асгарда.' },
    { id: 'intro_3', speaker: 'raven', text: 'Гиблое место. Скукота. Враг сюда забредает раз в сто лет.' },
    { id: 'intro_4', speaker: 'raven', text: 'Ни боёв, ни славы. Прочахнем тут до самого Рагнарёка.' },
    { id: 'intro_5', speaker: 'raven', text: '...Так. А это ещё кто?' },
  ],

};

// ── подстановка имён ──
function fill(text) {
  return text
    .replaceAll('{player}', SPEAKERS.player.name)
    .replaceAll('{raven}',  SPEAKERS.raven.name);
}

// Достать готовые реплики группы по ключу (имена уже подставлены).
// Вернёт [] если ключа нет — код не упадёт.
export function getLines(key) {
  const group = DIALOGUES[key];
  if (!group) return [];
  return group.map(line => ({
    ...line,
    name: SPEAKERS[line.speaker]?.name ?? '',
    text: fill(line.text),
  }));
}

// Достать одну реплику по её id (если нужно поштучно).
export function getLine(key, id) {
  return getLines(key).find(line => line.id === id) ?? null;
}
