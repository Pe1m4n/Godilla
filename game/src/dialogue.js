// ── подстановка имён ──
function fill(text) {
  return text
    .replaceAll('{player}', SPEAKERS.player.name)
    .replaceAll('{raven}',  SPEAKERS.raven.name);
}

// Достать готовые реплики группы по ключу (имена уже подставлены).
// Вернёт [] если ключа нет — код не упадёт.
export function getLines(key) {
  const group = DIALOGUES[key];
  if (!group) return [];
  return group.map(line => ({
    ...line,
    name: SPEAKERS[line.speaker]?.name ?? '',
    portrait: SPEAKERS[line.speaker]?.portrait ?? 'crow',
    text: fill(line.text),
  }));
}

// Достать одну реплику по её id (если нужно поштучно).
export function getLine(key, id) {
  return getLines(key).find(line => line.id === id) ?? null;
}
